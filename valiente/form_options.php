const VALIENTE_ADDRESS_SUFFIX = ', Negros Oriental, Region VII (Central Visayas), Philippines';

function valiente_format_address(string $municipality, string $barangay): string
{
    return 'Barangay ' . $barangay . ', ' . $municipality . VALIENTE_ADDRESS_SUFFIX;
}

/**
 * @return array{municipality: string, barangay: string}
 */
function valiente_parse_address(?string $full): array
{
    $out = ['municipality' => '', 'barangay' => ''];
    $full = trim((string) $full);
    if ($full === '') {
        return $out;
    }
    $suf = VALIENTE_ADDRESS_SUFFIX;
    $ls = strlen($suf);
    if (strlen($full) < $ls || substr_compare($full, $suf, -$ls) !== 0) {
        return $out;
    }
    $core = substr($full, 0, -$ls);
    $comma = strpos($core, ', ');
    if ($comma === false) {
        return $out;
    }
    $barPart = trim(substr($core, 0, $comma));
    $munPart = trim(substr($core, $comma + 2));
    if (stripos($barPart, 'Barangay ') === 0) {
        $barPart = trim(substr($barPart, 9));
    }
    $out['barangay'] = $barPart;
    $out['municipality'] = $munPart;
    return $out;
}

/**
 * @param array<string, list<string>> $barangays_by_municipality
 */
function valiente_build_address_from_post(array $barangays_by_municipality, string $municipality, string $barangay): string
{
    $municipality = trim($municipality);
    $barangay = trim($barangay);
    if ($municipality === '' || $barangay === '') {
        return '';
    }
    if (!isset($barangays_by_municipality[$municipality])) {
        return '';
    }
    if (!in_array($barangay, $barangays_by_municipality[$municipality], true)) {
        return '';
    }
    return valiente_format_address($municipality, $barangay);
}

